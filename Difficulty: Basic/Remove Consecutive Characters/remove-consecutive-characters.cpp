class Solution{
    public:
    string removeConsecutiveCharacter(string S)
    {
        // code here.
        string res=S;
        S.clear();
        for(int i=0;i<res.size();i++){
            if(res[i]==res[i-1]) continue;
            else S+=res[i];
        }
        return S;
    }
};

